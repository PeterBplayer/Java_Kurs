public class Zmienne {

    public static void main(String[] args) {
        int firstNumber, secondNumber, result;

        String name = "Piotrek";
        System.out.println(name);
        firstNumber = 1;
        secondNumber = 2;
        result = firstNumber + secondNumber;

        name = "Kinga";
        System.out.println(name);
        System.out.println(result);
    }
}
