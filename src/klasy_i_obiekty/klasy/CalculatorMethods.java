package klasy_i_obiekty.klasy;

public class CalculatorMethods {
    public float add(float var1, float var2) {
        return var1 + var2;
    }

    public float sub(float var1, float var2) {
        return var1 - var2;
    }

    public float mult(float var1, float var2) {
        return var1 * var2;
    }

    public float div(float var1, float var2) {
        return var1 / var2;
    }

    public float mod(float var1, float var2) {
        return var2 % var1;
    }
}
